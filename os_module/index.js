const os  = require("os");

console.log(os.arch()); // os architecture
console.log(os.freemem())  // free ram
console.log(os.homedir());  // home dir

console.log(os.type()) ; // window type window nt means window network

console.log(os.totalmem());  // total RAM
 console.log(os.hostname()); // hostname
 console.log(os.version()); // Window version like window 11 home
 console.log(os.tmpdir()) // temp dir
 console.log(os.platform()); // platform
 console.log(os.uptime()); // uptime